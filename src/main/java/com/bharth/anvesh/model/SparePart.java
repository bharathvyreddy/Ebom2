package com.bharth.anvesh.model;

public class SparePart {
	
	private String partNumber;
	private String modelYear;
	private String vehicleFamily;
	private String otherModelYear;
	private String otherVehicleFamily;
	private String lineUpCodeOne;
	private String lineUpCodeTwo;
	private String lineUpCodeThree;
	private String volume;
	private String unitOfMeasure;
	
	public String getPartNumber() {
		return partNumber;
	}
	public void setPartNumber(String partNumber) {
		this.partNumber = partNumber;
	}
	public String getModelYear() {
		return modelYear;
	}
	public void setModelYear(String modelYear) {
		this.modelYear = modelYear;
	}
	public String getVehicleFamily() {
		return vehicleFamily;
	}
	public void setVehicleFamily(String vehicleFamily) {
		this.vehicleFamily = vehicleFamily;
	}
	public String getOtherModelYear() {
		return otherModelYear;
	}
	public void setOtherModelYear(String otherModelYear) {
		this.otherModelYear = otherModelYear;
	}
	public String getOtherVehicleFamily() {
		return otherVehicleFamily;
	}
	public void setOtherVehicleFamily(String otherVehicleFamily) {
		this.otherVehicleFamily = otherVehicleFamily;
	}
	public String getLineUpCodeOne() {
		return lineUpCodeOne;
	}
	public void setLineUpCodeOne(String lineUpCodeOne) {
		this.lineUpCodeOne = lineUpCodeOne;
	}
	public String getLineUpCodeTwo() {
		return lineUpCodeTwo;
	}
	public void setLineUpCodeTwo(String lineUpCodeTwo) {
		this.lineUpCodeTwo = lineUpCodeTwo;
	}
	public String getLineUpCodeThree() {
		return lineUpCodeThree;
	}
	public void setLineUpCodeThree(String lineUpCodeThree) {
		this.lineUpCodeThree = lineUpCodeThree;
	}
	public String getVolume() {
		return volume;
	}
	public void setVolume(String volume) {
		this.volume = volume;
	}
	public String getUnitOfMeasure() {
		return unitOfMeasure;
	}
	public void setUnitOfMeasure(String unitOfMeasure) {
		this.unitOfMeasure = unitOfMeasure;
	}
	
}
